public class natural{
	public static void main(String args[]){
		int i;
		System.out.println("\t\tPrinting first 10 natural number by using for loop:- ");
		for(i=1; i<=10; i++){
			System.out.println(" "+i);
		}
		i=1;
		System.out.println("\t\tPrinting first 10 natural number by using WHILE LOOP:- ");
		while(i<=10){
			System.out.println(" "+i);
			i++;
		}
		i=1;
		System.out.println("\t\tPrinting first 10 natural number by using DO WHILE LOOP:- ");
		do{
			System.out.println(" "+i);
			i++;
		}while(i<=10);
		
	}
	
}
